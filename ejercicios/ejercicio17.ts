// Ejercicio 17: Número par o impar
function esPar(num: number): boolean {
    return num % 2 === 0;
}

console.log(esPar(7)); // false

// Explicación:
// Estoy viendo cómo saber si un número es par o impar. Uso el % para ver si el resto es 0.
// Si da 0, entonces es par. Si no, es impar. Lo muestro con console.log.
